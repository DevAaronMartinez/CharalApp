/**
 * Catálogo ampliado de medicamentos (México / LATAM).
 * Información educativa — verificar siempre empaque y médico.
 */
function med(id, data) {
  return { id: `med-${id}`, ...data };
}

let barcodeSeq = 380200;

function nextBarcode() {
  barcodeSeq += 1;
  return String(barcodeSeq);
}

/** Entrada compacta con defaults seguros */
function m(
  id,
  name,
  {
    brands = [],
    ingredients = [],
    keywords = [],
    conditions = [],
    clinical = null,
    dose,
    form = 'Tableta',
    desc,
    warns = [],
    interactions = [],
    shape = 'Ver empaque oficial',
  }
) {
  return med(id, {
    name,
    brandNames: brands.length ? brands : [name.split('/')[0].trim()],
    activeIngredients: ingredients,
    ocrKeywords: keywords,
    barcode: nextBarcode(),
    conditionIds: conditions,
    ...(clinical ? { clinicalUse: clinical } : {}),
    dosage: dose,
    form,
    color: '#FFFFFF',
    shape,
    warnings: warns.length
      ? warns
      : ['Verificar contraindicaciones en el prospecto', 'Consultar a tu médico si hay dudas'],
    description: desc,
    interactions,
  });
}

